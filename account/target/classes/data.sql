 CREATE TABLE IF NOT EXISTS ACCOUNTS (
    id int AUTO_INCREMENT,
    accountName varchar(40),
    accountNumber varchar(40),
    balance int,
    interest int, 
    lastStatementDate Date,
    paymentDate Date,
    PRIMARY KEY(id)
);

